package com.teslenko.operators;

public class E09_MinMaxExponents {
    public static void main(String[] args) {
        System.out.println("Float max exponent constant = " + Float.MAX_EXPONENT);
        System.out.println("Float max exponent = " + Float.MAX_VALUE);
        System.out.println("Float min exponent constant = " + Float.MIN_EXPONENT);
        System.out.println("Float min exponent = " + Float.MIN_VALUE);

        System.out.println("Double max exponent constant = " + Double.MAX_EXPONENT);
        System.out.println("Double max exponent = " + Double.MAX_VALUE);
        System.out.println("Double min exponent constant = " + Double.MIN_EXPONENT);
        System.out.println("Double min exponent = " + Double.MIN_VALUE);
    }
}
